package ConditionalStatement;

public class SwitchcaseStatement {
	public static void main(String[] args) {
		 int a=7;
		 switch (a) {
		 case 9:
			 System.out.println(" This is  John's house ");
			 break;
		  case 8:
			 System.out.println("This is ankit's house");
			 break;
		 case 7:
			 System.out.println(" This is reema's house ");
			 break;
		 case 5:
			 System.out.println(" This is payal's house ");
			 break;
			 default:
				 System.out.println(" Invalid Data");
}}}
    
