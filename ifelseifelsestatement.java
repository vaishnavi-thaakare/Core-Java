package ConditionalStatement;

public class ifelseifelsestatement {
     public static void main(String[] args) {
    	 int number=5;
    	 if (number==1) {
    		 System.out.println(" The given number "+number +" is associate with white color");		 
    	 }
    	 else if(number==2) {
    		 System.out.println("The given number "+number +" is associate with pink color");
    	 }
    	 else if(number==3) {
    		 System.out.println("The given number is "+number+" is associate with blue color");
    	 }else {
    		 System.out.println("The number is not associate with any color");
    	 }
     }
}
